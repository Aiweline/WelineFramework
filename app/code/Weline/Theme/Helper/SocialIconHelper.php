    /**
     * 获取指定平台的 SVG 图标
     *
     * @param string $platform 平台标识
     * @return string|null SVG 代码
     */
    public static function getIcon(string $platform): ?string
    {
        $icons = self::getAvailableIcons();
        return $icons[$platform]['svg'] ?? null;
    }

    /**
     * 获取指定平台的品牌颜色
     *
     * @param string $platform 平台标识
     * @return string|null 颜色代码
     */
    public static function getColor(string $platform): ?string
    {
        $icons = self::getAvailableIcons();
        return $icons[$platform]['color'] ?? null;
    }

    /**
     * 获取指定平台的名称
     *
     * @param string $platform 平台标识
     * @return string|null 平台名称
     */
    public static function getName(string $platform): ?string
    {
        $icons = self::getAvailableIcons();
        return $icons[$platform]['name'] ?? null;
    }

    /**
     * 渲染 SVG 图标
     *
     * @param string $platform 平台标识
     * @param array $attributes HTML 属性
     * @return string 完整的 SVG HTML
     */
    public static function renderIcon(string $platform, array $attributes = []): string
    {
        $svg = self::getIcon($platform);
        if (!$svg) {
            return '';
        }

        // 添加默认类名
        $class = $attributes['class'] ?? 'social-icon';
        $width = $attributes['width'] ?? '24';
        $height = $attributes['height'] ?? '24';
        
        // 插入属性到 SVG 标签
        $svg = str_replace(
            '<svg',
            sprintf('<svg class="%s" width="%s" height="%s"', htmlspecialchars($class), htmlspecialchars($width), htmlspecialchars($height)),
            $svg
        );

        return $svg;
    }
