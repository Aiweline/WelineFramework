             * 当平均 CPU 使用率超过此值时触发扩容
             * 默认：80.0
             */
            'scale_up_threshold_cpu' => 80.0,

            /**
             * 缩容 CPU 阈值（百分比）
             *
             * 当平均 CPU 使用率低于此值时触发缩容
             * 默认：30.0
             */
            'scale_down_threshold_cpu' => 30.0,

            /**
             * 扩容队列阈值（倍数）
             *
             * 当总请求队列长度超过 Worker 数量 * 此值时触发扩容
             * 默认：10
             *
             * 示例：4 个 Worker，阈值 10，则队列长度超过 40 时扩容
             */
            'scale_up_threshold_queue' => 10,

            /**
             * 冷却期（秒）
             *
             * 两次扩缩容操作之间的最小间隔，防止频繁扩缩容导致抖动
             * 默认：60
             *
             * 建议值：
             * - 开发环境：30-60
             * - 生产环境：60-120
             */
            'cooldown_seconds' => 60,
        ],

