// 配置调优建议
// ============================================================

/**
 * 1. CPU 阈值调优
 *
 * - 扩容阈值（scale_up_threshold_cpu）：
 *   - 过低（< 60%）：资源浪费，频繁扩容
 *   - 过高（> 90%）：响应慢，用户体验差
 *   - 推荐：70-80%
 *
 * - 缩容阈值（scale_down_threshold_cpu）：
 *   - 过低（< 20%）：缩容不及时，资源浪费
 *   - 过高（> 50%）：频繁缩容，不稳定
 *   - 推荐：20-30%
 *
 * - 阈值差距：
 *   - 扩容阈值 - 缩容阈值应 >= 30%，避免抖动
 */

/**
 * 2. 队列阈值调优
 *
 * - 短请求场景（< 100ms）：
 *   - 推荐：3-5，快速扩容
 *
 * - 中等请求场景（100-500ms）：
 *   - 推荐：5-10，平衡扩容
 *
 * - 长请求场景（> 500ms）：
 *   - 推荐：10-20，避免过度扩容
 */

/**
 * 3. 冷却期调优
 *
 * - 流量稳定场景：
 *   - 推荐：60-120 秒，避免频繁扩缩容
 *
 * - 流量波动场景：
 *   - 推荐：30-60 秒，快速响应
 *
 * - 流量突发场景：
 *   - 推荐：30-45 秒，快速扩容
 */

/**
 * 4. Worker 数量调优
 *
 * - 最小 Worker 数（min_workers）：
 *   - 开发环境：1-2
 *   - 生产环境：2-4（保证高可用）
 *   - 高流量环境：4-8（应对突发流量）
 *
 * - 最大 Worker 数（max_workers）：
 *   - CPU 密集型：CPU 核心数 * 1.5 ~ 2
 *   - I/O 密集型：CPU 核心数 * 2 ~ 4
 *   - 混合型：CPU 核心数 * 2 ~ 3
 *
 * - 注意事项：
 *   - 考虑内存限制：每个 Worker 约占用 50-200MB 内存
 *   - 考虑端口限制：Dispatcher 模式下每个 Worker 占用一个端口
 *   - 考虑数据库连接限制：每个 Worker 可能持有多个数据库连接
 */

/**
 * 5. 监控和调优流程
 *
 * 1. 启用自动扩缩容，使用保守配置
 * 2. 观察 1-2 周，收集负载数据
 * 3. 使用 `php bin/w server:scale --status` 查看实时指标
 * 4. 根据实际负载调整阈值
 * 5. 逐步优化，避免激进调整
 */

/**
 * 6. 常见问题排查
 *
 * Q: Worker 数量频繁变化（抖动）
 * A: 增加冷却期（cooldown_seconds），增大扩缩容阈值差距
 *
 * Q: 负载高但不扩容
 * A: 检查是否达到 max_workers，降低扩容阈值
 *
 * Q: 负载低但不缩容
 * A: 检查是否达到 min_workers，提高缩容阈值
 *
 * Q: 扩容后负载仍然高
 * A: 检查是否有性能瓶颈（数据库、缓存、网络），考虑增加 max_workers
 *
 * Q: 内存占用过高
 * A: 减少 max_workers，检查内存泄漏
 */
