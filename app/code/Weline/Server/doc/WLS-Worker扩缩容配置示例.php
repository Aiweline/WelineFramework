             *
             * true  - 启用自动扩缩容，Master 会根据负载自动调整 Worker 数量
             * false - 禁用自动扩缩容，只能通过 CLI 命令手动调整（默认）
             */
            'enabled' => false,

            /**
             * 最小 Worker 数量
             *
             * 自动缩容时不会低于此值，防止缩容到 0 个 Worker
             * 默认：1
